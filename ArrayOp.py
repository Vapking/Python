from numpy import *

# Addition
arr1 = array([1,2,3,4,5])
arr2 = arr1 + 5
print("Array before Addition :",arr1)
print("Array After Adding by 5 :",arr2)

# Multiplication
arr2 = arr1 * 5
print("Array before Multiplication :",arr1)
print("Array After Multiplying by 5 :",arr2)

# Subtraction
arr2 = arr1 - 1
print("Array before Subtraction :",arr1)
print("Array After Subtracting by 5 :",arr2)

# Division
arr2 = arr1 // 2
print("Array before Division :",arr1)
print("Array After Dividing by 5 :",arr2)
